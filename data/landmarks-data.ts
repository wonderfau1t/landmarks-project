// Helper function to get all articles across all landmarks
export function getAllArticles(): Article[] {
  return landmarksData.flatMap((landmark) =>
    landmark.articles.map((article) => ({
      ...article,
      landmarkId: landmark.id, // Add the landmark ID to each article
    })),
  )
}

// Helper function to find a specific landmark by ID
export function getLandmarkById(id: string): Landmark | undefined {
  return landmarksData.find((landmark) => landmark.id === id)
}

// Helper function to find a specific article by ID
export function getArticleById(id: string): { article: Article; landmark: Landmark } | undefined {
  for (const landmark of landmarksData) {
    const article = landmark.articles.find((article) => article.id === id)
    if (article) {
      return {
        article: {
          ...article,
          landmarkId: landmark.id, // Add the landmark ID to the article
        },
        landmark,
      }
    }
  }
  return undefined
}
